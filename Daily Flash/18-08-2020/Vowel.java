class Vowel {
	
	public static void main(String[] args){
		
		char x = 'E';
		
		switch(x) {
			
			case 'A' :
					System.out.println(x+" is a vowel");
					break;
					
			case 'E' :
					System.out.println(x+" is a vowel");
					break;
					
			case 'I' :
					System.out.println(x+" is a vowel");
					break;
					
			case 'O' :
					System.out.println(x+" is a vowel");
					break;
					
			case 'U' :
					System.out.println(x+" is a vowel");
					break;
					
			default :
					System.out.println(x+" is a consonant");
		}
	}
}